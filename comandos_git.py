#git comandos para trabajar con repositorios
#git init: inicializa un repositorio git en el directorio actual
#git add: agrega archivos al área de preparación (staging area)
#git commit: guarda los cambios en el repositorio
#git status: muestra el estado de los archivos en el repositorio
#git log: muestra el historial de commits
#git branch: muestra las ramas del repositorio
#git checkout: cambia a una rama específica 
#git merge: fusiona una rama con la rama actual
#git push: envía los cambios al repositorio remoto
#git pull: obtiene los cambios del repositorio remoto y los fusiona con la rama actual
#git clone: clona un repositorio remoto a tu máquina local
#git remote: gestiona los repositorios remotos
#git reset: deshace cambios en el área de preparación o en el repositorio   
#git stash: guarda temporalmente los cambios no confirmados
#git tag: crea una etiqueta para un commit específico
#git diff: muestra las diferencias entre archivos o commits
#git config: configura opciones de git, como el nombre de usuario y el correo electrónico
#git rm: elimina archivos del repositorio
#git mv: mueve o renombra archivos en el repositorio
#git show: muestra información sobre un commit específico
#git cherry-pick: aplica un commit específico de otra rama a la rama actual
#git rebase: aplica los cambios de una rama sobre otra, reescribiendo el historial
#git bisect: encuentra el commit que introdujo un error en el código
#git reflog: muestra el historial de referencias, útil para recuperar commits perdidos
